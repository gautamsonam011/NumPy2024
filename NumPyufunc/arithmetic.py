# Simple Arithmetic 

# Addition :---------->

# add() method 

import numpy as np 

arr1 = np.array([23, 45, 78, 90, 32, 21])
arr2 = np.array([43, 56, 87, 34, 51, 79])

newarr = np.add(arr1, arr2)
print(newarr)

# Substraction:---------> 
# substruct() method : 

arr1 = np.array([1, 2, 3, 4, 6, 7])
arr2 = np.array([2, 4, 6, 7, 1, 2])

newarr = np.subtract(arr1, arr2)
print(newarr)

# Multiplication:--------------> 

# multiply() method: 

a1 = np.array([1, 2, 3, 4, 5])
a2 = np.array([6, 7, 8, 9, 10])

newarr = np.multiply(a1, a2)
print(newarr)

# Division:-------------> 

# divide() function 

arr1 = np.array([43, 56, 87, 90, 21])
arr2 = np.array([3, 4, 7, 8, 2])

newarr = np.divide(arr1, arr2)
print(newarr)

# Power:-------------> 

# power() function 

arr1 = np.array([12, 11, 6, 8, 9])
arr2 = np.array([2, 4, 6, 2, 2])

newarr = np.power(arr1, arr2)
print(newarr)

